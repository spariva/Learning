TTITLE 'CIF:S02021943.'-
SKIP 1 'DEEP PURPLE S.A.'-
SKIP 1 'C/LUDOVICO. 28019. MADRID.'-
SKIP 2

COLUMN N_PEDIDO HEADING 'NUMERO DE PEDIDO:' FORMAT A20
COLUMN FECHA_PED HEADING 'FECHA:'

COLUMN NOMBRE_C HEADING 'CLIENTE:' FORMAT A30
COLUMN DIRECCION_C HEADING 'DIRECCION:'

COLUMN NOMBRE_P HEADING 'PRODUCTO'
COLUMN CANTIDAD HEADING 'CANTIDAD'
COLUMN PRECIO HEADING 'PRECIO POR UNIDAD'

SELECT N_PEDIDO, FECHA_PED FROM CAB_PEDIDO 
WHERE &N_PEDIDO=N_PEDIDO 
ORDER BY N_PEDIDO;
