SELECT NOMBRE_C, DIRECCION_C FROM CLIENTE, CAB_PEDIDO 
WHERE &N_PEDIDO=N_PEDIDO 
AND CLIENTE.DNI=CAB_PEDIDO.DNI;

SELECT NOMBRE_P, CANTIDAD, PRECIO FROM PRODUCTO, LINEA_PEDIDO 
WHERE &N_PEDIDO=N_PEDIDO 
AND PRODUCTO.CODIGO_P=LINEA_PEDIDO.CODIGO_P 
ORDER BY N_LINEA_PEDIDO;

